func KeyExpansion(inkey [][]byte) (expandedkey [][]byte){
	key_len := len(inkey) // number of 32 bit columns in key 4(128 bit), 6(192 bit), or 8(256 bit)
	rounds := key_len + 6
	
	expandedkey = make([][]byte, 4 * (rounds + 1))
	
	var i int
	// copy the key into the first part of the expanded key
	for i = 0; i < key_len; i++ {
		expandedkey[i] = inkey[i]
	}
	for ; i < len(expandedkey); i++ {
		tempcol := expandedkey[i-1]
		if (i % key_len) == 0 {
			rconcol := []byte{rcon[i/key_len], 0x00, 0x00, 0x00}
			tempcol = XorCol( SubCol( RotateCol(tempcol)), rconcol)
		} else if (key_len == 8) && (i % key_len == 4) {
			tempcol = SubCol(tempcol)
		}
		expandedkey[i] = XorCol(expandedkey[i-key_len], tempcol)
	}
	return expandedkey
}

func RotateCol(col []byte) (outcol []byte) {
	outcol = []byte{col[1], col[2], col[3], col[0]}
	return outcol
}

func XorCol(col1, col2 []byte) (outcol []byte) {
	outcol = make([]byte, len(col1))
	for i := 0; i < len(col1); i++ {
		outcol[i] = col1[i] ^ col2[i]
	}
	return outcol
}
